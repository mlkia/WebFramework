let animals = ["dog", "cat", "zebra", "horse", "cow"];
//The shift() method removes the first element from an array and returns that removed element.
console.log("The animal who has deleted: " + animals.shift()); //Här ta bort första värdet från arrayn och returnerar det "dog".

let theAnimals = ["dog", "cat", "zebra", "horse", "cow"];
theAnimals.shift();
console.log("My favourite animal is: " + theAnimals[0]); //Här tar bort första värdet och arrayn börjar med "cat". 

